/**
 * @brief Get the Fontptr object
 * 
 * @param font 
 * @param ch 
 * @return unsigned char const* 
 */
unsigned char const *getFontptr(ASCIIFont font, unsigned char ch)
{               
                                /*计算行所占字节，不满8补1*/   
    unsigned long int offset = (_fontParam[font].width + 7) / 8 * _fontParam[font].high;
    return _fontParam[font].baseAddr + ((ch - ' ') * offset);
}
/**
 * @brief Get the Font Width object
 * 
 * @param font 
 * @return unsigned char 
 */
unsigned char getFontWidth(ASCIIFont font){
    return _fontParam[font].width;
}
/**
 * @brief Get the Font High object
 * 
 * @param font 
 * @return unsigned char 
 */
unsigned char getFontHigh(ASCIIFont font){
    return _fontParam[font].high;
}
unsigned char getFontByteNum(ASCIIFont font){
    return (_fontParam[font].width + 7) / 8 * _fontParam[font].high;
}

